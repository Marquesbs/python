"""
O for em Python é uma estrutura de repetição que permite iterar sobre uma sequência (como uma lista, tupla, string ou um range de números). A sintaxe básica do for é a seguinte:

for item in sequencia:
    # faça algo com item
"""
# Exemplo em lista
frutas = ['maçã', 'banana', 'laranja']
for fruta in frutas:
    print(fruta)

# Exemplo em string
palavra = "Python"
for letra in palavra:
    print(letra)

# Exemplo em range
for i in range(5):  # Isso irá de 0 a 4
    print(i)